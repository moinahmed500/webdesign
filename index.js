function greetUser() {
    var currentTime = new Date();
    var currentHour = currentTime.getHours();

    if (currentHour < 12) {
        return "Good morning!";
    } else if (currentHour >= 12 && currentHour < 17) {
        return "Good afternoon!";
    } else if (currentHour >= 17 && currentHour < 20) {
        return "Good evening!";
    } else {
        return "Good night!";
    }
}

